package EX3_Parti1;

public class Point {
	
	private int x,y;
	// constucteur 
	public Point(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	//getters setters
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
	

}
